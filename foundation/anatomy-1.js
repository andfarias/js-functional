// Function declaration

function sayHello() {
    console.log('Hello!')
}

function sayHelloTo(name) {
    console.log(`Hello ${name}`)
}

sayHello()
sayHelloTo('Anderson')

function returnHi() {
    return 'Hi'
}

console.log(returnHi())

function returnHiTo(name) {
    return `Hi ${name}`
}

console.log(returnHiTo('Anderson'))